namespace Minecraft {
    public struct Vertex {
        public float X, Y, Z, U, V, R, G, B, S, D;

        public Vertex(float x, float y, float z, float u, float v, float r, float g, float b, float s, float d) {
            X = x;
            Y = y;
            Z = z;
            U = u;
            V = v;
            R = r;
            G = g;
            B = b;
            S = s;
            D = d;
        }
    }
}